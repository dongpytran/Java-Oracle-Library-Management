    private void jBtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUpdateActionPerformed
        if(jTable1.getSelectedRow() != -1){
            String emCode = txtCode.getText().trim();
            String lastName = txtLast.getText().trim();
            String first = txtFirst.getText().trim();
            String birth = txtBirth.getText().trim();
            String gender = boxGender.getSelectedItem().toString().trim();
            String adress = txtAdress.getText().trim();
            String phone = txtPhone.getText().trim();
            String email = txtEmail.getText().trim();
            Employees em = new Employees();

            em.setEmCode(emCode);
            em.setEmLastName(lastName);
            em.setEmFirstName(first);
            em.setBirth(birth);
            em.setGender(gender);
            em.setAdress(adress);
            em.setPhone(phone);
            em.setEmail(email);
            if(emDLL.updateData(em)){
                JOptionPane.showMessageDialog(this, "Updated!");
                System.out.println("Updated Employee Name : "+first);
                tableShow();
            }else{
                JOptionPane.showMessageDialog(this, "Update Failed!");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Choose an Employee!");
        }
    }//GEN-LAST:event_jBtnUpdateActionPerformed

