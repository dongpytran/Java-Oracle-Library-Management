    public void tableShow(){
        listEm = emDLL.getAll();
        Vector clums = new Vector();
        Vector data = new Vector();
        clums.add("EmCode");
        clums.add("LastName");
        clums.add("FirstName");
        clums.add("Birth");
        clums.add("Gender");
        clums.add("Adress");
        clums.add("Phone");
        clums.add("Email");
        for(Employees e : listEm){
            Vector row = new Vector();
            row.add(e.getEmCode().trim());
            row.add(e.getEmLastName().trim());
            row.add(e.getEmFirstName().trim());
            row.add(e.getBirth().trim());
            row.add(e.getGender().trim());
            row.add(e.getAdress().trim());
            row.add(e.getPhone().trim());
            row.add(e.getEmail().trim());
            data.add(row);
        }
        DefaultTableModel dtm = new DefaultTableModel(data, clums);
        jTable1.setModel(dtm);
        jTable1.setDefaultEditor(Object.class, null);
    }
    
    private void displayShow(){
        if(jTable1.getSelectedRow() != -1){
            int row = jTable1.getSelectedRow();
            listEm = emDLL.getAll();
            txtCode.setText(listEm.get(row).getEmCode().trim());
            txtLast.setText(listEm.get(row).getEmLastName().trim());
            txtFirst.setText(listEm.get(row).getEmFirstName().trim());
            txtBirth.setText(listEm.get(row).getBirth().trim());
            txtAdress.setText(listEm.get(row).getAdress().trim());
            txtPhone.setText(listEm.get(row).getPhone().trim());
            txtEmail.setText(listEm.get(row).getEmail().trim());
            boxGender.setSelectedItem(listEm.get(row).getGender().trim());
            txtCode.setEditable(false);
        }
    }
    private void genderShow(){
        listEm = emDLL.getGender();
        for(Employees e : listEm){
            boxGender.addItem(e.getGender().trim());
        }
    }
