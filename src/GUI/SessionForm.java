    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selected = jTable1.getSelectedRow();
        if(selected < 0){
            JOptionPane.showMessageDialog(this, "Choose a session");
            return;
        }
        listSession = ssDLL.getAll();
        int sid = listSession.get(selected).getSID();
        long serial = listSession.get(selected).getSerial();
        String query = "alter system kill session '"+ sid +"," + serial +"'";
        
        try {
                // Set the connection properties.
                // required: SYSDBA
                Properties prop = new Properties();
                prop.setProperty("user","sys");
                prop.setProperty("password","Oracle123");
                prop.setProperty("internal_logon","sysdba");

                OracleDataSource ods = new OracleDataSource();
                ods.setConnectionProperties(prop);
                ods.setURL("jdbc:oracle:thin:@localhost:1521:orcl");
                OracleConnection ocon = (OracleConnection)ods.getConnection();
                System.out.println("Connected");     

                // shutdown the database
                Statement stmt = ocon.createStatement();
                stmt.execute(query);
                stmt.close();
                System.out.println("Killed session " + sid + " Successfully!");
                JOptionPane.showMessageDialog(this, "Killed session " + sid + " Successfully!");
                ocon.close();
                ods.close();
                showSession();
              } catch(SQLException e) {
                System.out.println(e.getMessage());
                JOptionPane.showMessageDialog(this, e);
              }
    }//GEN-LAST:event_jButton1ActionPerformed

