    private void showSession(){
        Vector clums = new Vector();
        Vector data = new Vector();
        listSession = ssDLL.getAll();
        clums.add("SID");
        clums.add("SERIAL#");
        clums.add("PADDR");
        clums.add("USERNAME");
        clums.add("MACHINE");
        clums.add("PROGRAM");
        clums.add("STATUS");
        for(Session b : listSession){
            Vector row = new Vector();
            row.add(b.getSID());
            row.add(b.getSerial());
            row.add(b.getPADDR().trim());
            row.add(b.getUser());
            row.add(b.getMachine().trim());
            row.add(b.getProgram().trim());
            row.add(b.getStatus().trim());
            data.add(row);
        }
        DefaultTableModel dtm = new DefaultTableModel(data, clums);
        jTable1.setModel(dtm);
        jTable1.setDefaultEditor(Object.class, null);
        jTable1.setAutoCreateRowSorter(true);
        jTable1.requestFocus();
    }
