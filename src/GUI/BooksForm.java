    public void tableShow(){
        Vector clums = new Vector();
        Vector data = new Vector();
        listBook = bookDll.getALL();
        clums.add("BookCode");
        clums.add("BookName");
        clums.add("Author");
        clums.add("Category");
        clums.add("PublishingHouse");
        clums.add("YearPublic");
        clums.add("Status");
        for(Books b : listBook){
            Vector row = new Vector();
            row.add(b.getBookCode().trim());
            row.add(b.getBookName().trim());
            row.add(b.getAuthor().trim());
            listCate = cateDLL.getByCode(b.getCategoryCode());
            if(listCate.size()>0){
                row.add(listCate.get(0).getCategoryName().trim());
            }
            else{
                row.add("Empty");
            }
            listPub = pubDll.getByCode(b.getPublisherCode());
            if(listPub.size()>0){
                row.add(listPub.get(0).getPubName().trim());
            }else{
                row.add("Empty");
            }     
            row.add(b.getPubYear().trim());
            row.add(b.getStus().trim());
            data.add(row);
        }
        DefaultTableModel dtm = new DefaultTableModel(data, clums);
        jTable1.setModel(dtm);
        jTable1.setDefaultEditor(Object.class, null);
        jTable1.setAutoCreateRowSorter(true);
        jTable1.requestFocus();
    }
