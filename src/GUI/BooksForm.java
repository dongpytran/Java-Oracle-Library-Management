    private void jBookBtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBookBtnAddActionPerformed
        if(bookDll.countCode(txtBookCode.getText().trim()) == 0){    
            listCate = cateDLL.getAll();
            listPub = pubDll.getAll();
            String bookCode = txtBookCode.getText().trim();
            String bookName = txtBookName.getText().trim();
            String author = txtAuthor.getText().trim();
            String year = txtPubYear.getText().trim();
            String s = txtStus.getText().trim();
            int rowCate = cateBox.getSelectedIndex();
            int rowPub = publishBox.getSelectedIndex();
            String cate = listCate.get(rowCate).getCategoryCode();
            String pub = listPub.get(rowPub).getPubCode();

            Books bk = new Books();
            bk.setBookCode(bookCode);
            bk.setBookName(bookName);
            bk.setAuthor(author);
            bk.setPubYear(year);
            bk.setStus(s);
            bk.setPublisherCode(pub);
            bk.setCategoryCode(cate);
            bk.setPicture(book_img);
            if(bookDll.AddData(bk)){
                JOptionPane.showMessageDialog(this, "Added!");
                System.out.println("Added Book Name : "+bookName);
                tableShow();
            }else{
                JOptionPane.showMessageDialog(this, "Add Failed!");
            }
        }else{
            JOptionPane.showMessageDialog(this, "BookCode: "+ txtBookCode.getText().trim() +" has already exist!");
        }
    }//GEN-LAST:event_jBookBtnAddActionPerformed

