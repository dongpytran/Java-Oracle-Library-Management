    private void displayShow(){
            if(jTable1.getSelectedRow() != -1){
                int row = jTable1.getSelectedRow();
                txtBookCode.setEditable(false);
                listBook = bookDll.getALL();
                txtBookCode.setText(listBook.get(row).getBookCode().trim());
                txtBookName.setText(listBook.get(row).getBookName());
                txtAuthor.setText(listBook.get(row).getAuthor());
                txtPubYear.setText(listBook.get(row).getPubYear());
                txtStus.setText(listBook.get(row).getStus());

                listCate = cateDLL.getByCode(listBook.get(row).getCategoryCode());
                if(listCate.size() > 0){
                    cateBox.setSelectedItem(listCate.get(0).getCategoryName());
                }else{
                    cateBox.setSelectedIndex(0);
                }

                listPub = pubDll.getByCode(listBook.get(0).getPublisherCode());
                if(listPub.size() >0){
                    publishBox.setSelectedItem(listPub.get(0).getPubName());
                }else{
                    publishBox.setSelectedIndex(0);
                }
                if(listBook.get(row).getPicture()!=null){
                    try {
                        byte[] img =  (listBook.get(row).getPicture());
                        ImageIcon imgIcon =  new ImageIcon(new ImageIcon(img).getImage().getScaledInstance(jBookAvatar.getWidth(), jBookAvatar.getHeight(),Image.SCALE_SMOOTH));
                        jBookAvatar.setIcon(imgIcon);
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, e);
                    }
                }else{
                    jBookAvatar.setIcon(null);
                }
            }
    }
