    private void chooseImgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooseImgActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File f = chooser.getSelectedFile();
        filename =  f.getAbsolutePath();
        ImageIcon img =  new ImageIcon(new ImageIcon(filename).getImage().getScaledInstance(jBookAvatar.getWidth(), jBookAvatar.getHeight(),Image.SCALE_SMOOTH));
        jBookAvatar.setIcon(img);
        try {
            File image = new File(filename);
            FileInputStream fis = new FileInputStream(image);
            ByteArrayOutputStream bos =  new ByteArrayOutputStream();
            byte[] buf = new  byte[1024];
            for(int readNum;(readNum=fis.read(buf))!=-1;){
                bos.write(buf,0,readNum);
                
            }
            book_img=bos.toByteArray();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_chooseImgActionPerformed

