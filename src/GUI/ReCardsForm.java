    private void jBtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAddActionPerformed
        if(reDLL.countNum(txtNum.getText().trim())==0){    
            String num = txtNum.getText().trim();
            String start = txtDayStart.getText().trim();
            String finish = txtDayFinish.getText().trim();
            String note = txtNote.getText().trim();
            ReCards r = new ReCards();
            r.setCardNum(num);
            r.setStartDay(start);
            r.setFinishDay(finish);
            r.setNote(note);
            if(reDLL.addData(r)){
                JOptionPane.showMessageDialog(this, "Added!");
                System.out.println("Added Card Number: "+num);
                tableShow();
            }else{
                JOptionPane.showMessageDialog(this, "Added Failed!");
            }
            setAllNew();
        }else{
            JOptionPane.showMessageDialog(this, "CardNumber: "+txtNum.getText().trim()+" has already exist!");
        }
    }//GEN-LAST:event_jBtnAddActionPerformed

    private void jBtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUpdateActionPerformed
        if(jTable1.getSelectedRow()!=-1){
            String num = txtNum.getText().trim();
            String start = txtDayStart.getText().trim();
            String finish = txtDayFinish.getText().trim();
            String note = txtNote.getText().trim();
            ReCards r = new ReCards();
            r.setCardNum(num);
            r.setStartDay(start);
            r.setFinishDay(finish);
            r.setNote(note);
            if(reDLL.updateData(r)){
                JOptionPane.showMessageDialog(this, "Updated!");
                System.out.println("Updated Card Number: "+num);
                tableShow();
            }else{
                JOptionPane.showMessageDialog(this, "Update Failed!");
            }
            setAllNew();
        }else{
            JOptionPane.showMessageDialog(this, "Choose a card!");
        }
    }//GEN-LAST:event_jBtnUpdateActionPerformed

    private void jBtnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnRemoveActionPerformed
        if(jTable1.getSelectedRow()!=-1){
            if(JOptionPane.showConfirmDialog(this, "Delete this card?")==JOptionPane.OK_OPTION){
                String n = txtNum.getText().trim();
                if(reDLL.removeData(n)){
                    JOptionPane.showMessageDialog(this, "Deleted!");
                    System.out.println("Deleted card Number: "+ n);
                    tableShow();
                }else{
                    JOptionPane.showMessageDialog(this, "Delete Failed");
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Choose a Card!");
        }
    }//GEN-LAST:event_jBtnRemoveActionPerformed

