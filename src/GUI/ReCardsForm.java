    public void tableShow(){
        listRe = reDLL.getAll();
        Vector clums = new Vector();
        Vector data = new Vector();
        clums.add("CardNumber");
        clums.add("DayStart");
        clums.add("DayFinish");
        clums.add("Note");
        for(ReCards r : listRe){
            Vector row = new Vector();
            row.add(r.getCardNum().trim());
            row.add(r.getStartDay().trim());
            row.add(r.getFinishDay().trim());  
            row.add(r.getNote().trim());
            data.add(row);
        }
        DefaultTableModel dtm = new DefaultTableModel(data,clums);
        jTable1.setModel(dtm);
        jTable1.setDefaultEditor(Object.class, null);
    }
    
    private void displayShow(){
        if(jTable1.getSelectedRow()!=-1){
            int row = jTable1.getSelectedRow();
            listRe = reDLL.getAll();
            txtNum.setText(listRe.get(row).getCardNum().trim());
            txtDayStart.setText(listRe.get(row).getStartDay().trim());
            txtDayFinish.setText(listRe.get(row).getFinishDay().trim());
            txtNote.setText(listRe.get(row).getNote().trim());
        }
    }
    private void setAllNew(){
        LocalDate date = LocalDate.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        String dateNow = date.format(formatter).toString();
        txtNum.setEditable(true);
        txtNum.setText("");
        txtDayStart.setText(dateNow);
        txtDayFinish.setText("");
        txtNote.setText("");
    }
